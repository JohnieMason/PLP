# EXAMPLE
# Variables hold data
age = 25           #int(integer)
height = 5.75      #float(decimal)
name = 'Charlie'   # str(decimal)
is_student = True  # Bool(boolean)

# print out the values
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is a student:", is_student)

# CONTROL FLOW -MAKING DECISION
# Conditions used : if -else statements (Controls how pur code runs)
# Example

temperature  = 30

if temperature > 25:
    print("It is a Hot day!")
else:
    print("It is a cool day")

# You can also add elif for multiple conditions. Example below;

# Grading system based on the marks
marks = 55
if marks > 70:
    print("Grade: Distinction")
elif marks >= 60:
    print("Grade: Credit")
elif marks >= 50:
    print("Grade: Pass")
else:
    print("Grade: Fail")

# LOOPS: Repeating loops like a pro!
# print numbers 0 to 4
#  (I) FOR LOOP
for i in range(5):
    print(i)  #Expected result: 0,1,2,3,4

 # (II) WHILE LOOP - Repeats as long as a condition is true.
countdown = 5

while countdown > 0:
    print("Counting down:", countdown)
    countdown -= 1

    print("Blast off!")


